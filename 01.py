# See impordib kilpkonna mooduli
import turtle

turtle.shape("turtle")
turtle.speed(0)
turtle.penup()
turtle.goto(-500,300)
turtle.pendown()
turtle.forward(200) #fd, pikslites
turtle.left(120) # Pööramine kraadides
turtle.fd(200)
turtle.left(120)
turtle.fd(200)

# Lõpetab kilpkonna, et ei jookseks kokku
turtle.done()